namespace Shouldly;

/// <summary>
/// Specifies the direction for sorting operations
/// </summary>
public enum SortDirection
{
    /// <summary>
    /// Sort in ascending order (smallest to largest)
    /// </summary>
    Ascending = 0,
    
    /// <summary>
    /// Sort in descending order (largest to smallest)
    /// </summary>
    Descending = 1
}